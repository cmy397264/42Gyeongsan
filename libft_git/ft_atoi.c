#include "libft.h"

int	ft_atoi(char *s)
{
	int	sum;
	int	is_minus;

	sum = 0;
	is_minus = 1;
	while ((*s >= 9 && *s <= 13) || *s == 32)
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			is_minus *= -1;
		s++;
	}
	while (*s && *s >= '0' && *s <= '9')
	{
		sum *= 10;
		sum += *s - '0';
		s++;
	}
	return (sum * is_minus);
}
